# 3. Check the type of variable assigned using input() function.

a = input("Please enter any thing for string : ")

print(type(a)) # Note for every input the type will be string always. 

b = int(input("Please enter any thing for integer : "))

print(type(b)) # Note for every input the type will be integer always.  

c = float(input("Please enter any thing for float : "))

print(type(c)) # Note for every input the type will be float always.  

d = bool(input("Please enter any thing for boolen : "))

print(type(d)) # Note for every input the type will be boolen always.  

